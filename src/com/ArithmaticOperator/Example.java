package com.ArithmaticOperator;

public class Example {
	
	public static void main(String[] args) {
		
		//+,-,/,*,%
		
		int var1=100;
		int var2=0;
		
		System.out.println(var1+var2);//90
		System.out.println(var1-var2);//110
		System.out.println(var1/var2);//-10
		System.out.println(var1%var2);//0
		System.out.println(var1*var2);//-1000
		
	}

}
