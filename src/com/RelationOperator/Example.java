package com.RelationOperator;

public class Example {
	
	public static void main(String[] args) {
	
		// <, <=, >, >=, ==, !=
	
		int var1=-1;
		int var2=-5;
		System.out.println(var1<var2);//t
		System.out.println(var1<=var2);//t
		System.out.println(var1>var2);//f
		System.out.println(var1>=var2);//f
		System.out.println(var1==var2);//f
		System.out.println(var1!=var2);//t
		
	}
	
}

