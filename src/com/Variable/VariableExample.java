package com.Variable;

public class VariableExample {
	
	public static void main(String[] args) {
	
		System.out.println("-----Integer Datatype Example-----");
		int intVar=10;
		System.out.println("The Value of intVar - "+intVar);
		intVar=30;
		System.out.println("The latest value of intVar - "+intVar);
		
		System.out.println("-----Char Datatype Example----");
		char charVar='l';
		System.out.println("The Value of charVar - "+charVar);
		charVar='L';
		System.out.println("The latest value of charVar - "+charVar);
		
		System.out.println("-----String datatype Example-----");
		String stringVar="Testing";
		System.out.println("The value of stringVar - "+stringVar);
		
		System.out.println("-----Float Datatype Example-----");
		float floatVar=12;
		System.out.println("The value of floatVar - "+floatVar);
		
		floatVar=12.1234601f;
		System.out.println("The latest value of floatVar - "+floatVar);
		
		System.out.println("-----Double datatype Example-----");
		double doubleVar=6.54427394234324;
		System.out.println("The value of doubleVar - "+doubleVar);
		
		System.out.println("-----Boolean Data type Example-----");
		boolean booleanVar=true;
		boolean booleanVar1=false;
		System.out.println("The value of booleanVar - "+booleanVar);
		System.out.println("The value of booleanVar1 - "+booleanVar1);
		
	}

}
